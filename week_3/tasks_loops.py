'''Задача 3
Во многих сотовых телефонах есть калькулятор чаевых.

Калькулятор чаевых - это программа, которая помогает рассчитать сумму
чаевых в ресторане. Чтобы использовать такую программу, нужно ввести
цену еды и процент чаевых, которые вы хотите оставить. Программа
рассчитает сумму чаевых и общий счет и выведет их на экран. Например,
если цена еды составляет 1000 рублей, а процент чаевых - 15%, то сумма
чаевых будет 150 рублей, а общий счет - 1150 рублей.'''

food_price = float(input("Введите цену еды:"))
tips_percent = float(input("Введите процент чаевых:"))

tips = food_price * tips_percent / 100
total = food_price + tips

print("Цена еды -", food_price, "рублей")
print("Чаевые -", tips, "рублей")
print("Итого -", total, "рублей")

