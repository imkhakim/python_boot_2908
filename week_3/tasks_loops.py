'''Задача 2
Напишите программу, которая просит пользователя ввести пароль. 
Если пользователь вводит правильный пароль, программа должна сообщить им, что они вошли в систему. 
В противном случае следует попросить их повторно ввести пароль. 
Пользователю дается только 5 попыток на ввод пароля, после чего программа должна сообщить им, 
что они удалены из системы.'''

i = 0
while i < 5:
    password = input("Введите пароль:")
    i += 1
    if password != "12345":
        if i in range(4):
            print("Неверный пароль, осталось ", 5-i, "попыток!")
        elif i == 4:
            print("Неверный пароль, осталась ", 5-i, "попытка!")
        else:
            print("Количество попыток превысило 5, Вы удалены из системы!")
    else:
        print("Вы вошли в ситему!")
        break

